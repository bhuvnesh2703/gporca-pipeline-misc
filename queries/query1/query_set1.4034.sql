SELECT
    A.COB_DATE,
    SUM (a.USD_NET_EXPOSURE) AS netexp
FROM cdwuser.U_DM_CC A
WHERE
    a.COB_DATE IN (
'2017-12-27', 
'2018-01-03', 
'2018-01-10', 
'2018-01-17', 
'2018-01-24', 
'2018-01-31', 
'2018-02-28'
) AND


    A.CCC_BUSINESS_AREA = 'CREDIT-CORPORATES' AND
    A.CREDIT_TRADING_FLAG = 'Junior Subs'
GROUP BY
    A.COB_DATE